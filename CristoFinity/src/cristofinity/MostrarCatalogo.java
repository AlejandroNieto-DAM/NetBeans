    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        this.setVisible(false);
             
        Informacion ventanaInfo = new Informacion();
       
      
       ventanaInfo.setResumenToLabel(MostrarCatalogo.lista.get(this.jList1.getSelectedIndex()).getResumen());
       
       if(MostrarCatalogo.lista.get(this.jList1.getSelectedIndex()) instanceof Serie){
            ventanaInfo.setTextMedia(atributosSerie((Serie) MostrarCatalogo.lista.get(this.jList1.getSelectedIndex())));
            ventanaInfo.setImageToJLabel(serie);

       } else if (MostrarCatalogo.lista.get(this.jList1.getSelectedIndex()) instanceof Pelicula){
            ventanaInfo.setTextMedia(atributosPelicula((Pelicula) MostrarCatalogo.lista.get(this.jList1.getSelectedIndex())));
            ventanaInfo.setImageToJLabel(pelicula);

       }else if (MostrarCatalogo.lista.get(this.jList1.getSelectedIndex()) instanceof Cancion){
            ventanaInfo.setTextMedia(atributosCancion((Cancion) MostrarCatalogo.lista.get(this.jList1.getSelectedIndex())));
            ventanaInfo.setImageToJLabel(cancion);

       }else if (MostrarCatalogo.lista.get(this.jList1.getSelectedIndex()) instanceof Videojuego){
            ventanaInfo.setTextMedia(atributosVideojuego((Videojuego) MostrarCatalogo.lista.get(this.jList1.getSelectedIndex())));
            ventanaInfo.setImageToJLabel(videojuego);
       }
       
       ventanaInfo.setVisible(true);
       
    }//GEN-LAST:event_jButton1ActionPerformed

