    private void sendButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendButtonActionPerformed
        
        passwd = jPasswordFieldPassword.getText();
        login = jTextFieldLogin.getText();
        
        User_Controller a = new User_Controller();
        a.getUsuarios(usuarios);        
        
        Boolean existe = false;
        String id_user = "";
        String passwd_user = "";
        
        for(int i = 0; i < usuarios.size() && existe == false ; i++){
            
            id_user = usuarios.get(i).getLogin();
            passwd_user = usuarios.get(i).getPasswd();
            
            if(login.equals(id_user) && passwd.equals(passwd_user)){
                existe = true;
            }
        }
        
        
        if(existe == true){
            
            this.setVisible(false);
            
            myCristoMessenger.setActualUser(login);
            myCristoMessenger.getFriendsOf();
            myCristoMessenger.setVisible(true);
        }
    }//GEN-LAST:event_sendButtonActionPerformed

