    public MostrarCatalogo() {
        

        initComponents();
        this.setTitle("Catalogo");
        this.setLocationRelativeTo(this);
        this.setResizable(false);
        
       
        
        int tamaño = MostrarCatalogo.lista.size();
        final String[] vector = new String[tamaño];
        for(int a = 0; a < tamaño; a++){
            vector[a] = ( "Titulo: " + MostrarCatalogo.lista.get(a).getTitulo());
        }
        
        this.jList1.setModel(new javax.swing.AbstractListModel(){
            String[] vect = vector;
            
            @Override
            public int getSize() {
                return vect.length;
            }

            @Override
            public Object getElementAt(int i) {
                return vect[i];
            }
        
        });
        
        
        jTextField1.setText(String.valueOf(lista.size()));
        jList1.setCellRenderer(new CristoFinity());
    }
    
    public String atributosMultimedia(Multimedia p){
   
        String media = "Titulo: " + p.getTitulo() + endl + "Genero: " + p.getGenero() + endl + "Fecha de publicacion: " +  p.getFechaPublicacion() + endl+ "Duracion en seg: " + p.getDuracionEnSegundos() + endl + "Pegi: " + p.getPegi() + endl;

        return media;
     
    }
    
    public String atributosSerie(Serie p){
        
        String atributos = atributosPelicula(p);
        
        atributos += "Temporadas: " + p.getTemporadas() + endl + "Capitulos: " + p.getCapitulos() + endl;
        return atributos;
    }
    
    public String atributosPelicula(Pelicula p){
       
        String atributos = atributosMultimedia(p);
        
        atributos += "Productor: " + p.getProductor() + endl + "Direccion: " + p.getDireccion() + endl + "Guion: " + p.getGuion() + endl + "Reparto:" + p.getReparto() + endl;
        
        return atributos;
    }
    
    public String atributosCancion(Cancion p){
        String atributos = atributosMultimedia(p);
        
        atributos += "Album: " + p.getAlbum() + endl + "Artista: " + p.getArtista() + endl;
        
        return atributos;
    }
    
    public String atributosVideojuego(Videojuego p){
        
        String atributos = atributosMultimedia(p);
        
        atributos += "Editor: " + p.getEditor() + endl + "Desarrollador: " + p.getDesarrollador() + endl + "Plataforma: " + p.getPlataforma() + endl + "Jugadores:" + p.getJugadores() + endl;
        
        return atributos;
    }

